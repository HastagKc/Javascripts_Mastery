// Destructuring is a feature in JavaScript that allows you to extract multiple values from arrays
// or properties from objects into distinct variables.
// It provides a more concise and readable way to access and assign
// values compared to traditional methods.

// Destructuring an array in js
let myArray = ["kshittiz", "Suressh", "Rahul", "Manish"];
let [stu1, stu2, stu3, stu4] = myArray;

console.log(stu1);
console.log(stu2);
console.log(stu3);
console.log(stu4);
