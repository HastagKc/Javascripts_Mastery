// Conditional statements in JavaScript are used to execute different blocks of code based on certain conditions. Here are the main types of conditional

// if statement
// if statement shows block of code when the condition is true
let age = 10;
if (age >= 18) {
  console.log("You are  now 18 or above 18");
}

// if else
// in if else statement, when the if statement is true it will show if block code, if false then it will show else block code

if (age >= 18) {
  console.log("You can get drive licences now");
} else {
  console.log("You can't get licences now");
}
