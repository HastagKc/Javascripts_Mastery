let num = 10;

num += 5; // num = num + 5
console.log(num); // 15

num -= 3; // num = num - 3
console.log(num); // 12

num *= 2; // num = num * 2
console.log(num); // 24

num /= 4; // num = num / 4
console.log(num); // 6

num %= 5; // num = num % 5
console.log(num); // 1

num **= 3; // num = num ** 3
console.log(num); // 1
