// for loop in js
// use when the number of iterations in known
// syntax
// for(initialization; condition; increment/decrement) {}

for (let i = 0; i < 10; i++) {
  console.log(i)
}

// looping in an array
let arr1 = [10, 20, 30, 55]
for (let i = 0; i < arr1.length; i++) {
  console.log(arr1[i])
}
