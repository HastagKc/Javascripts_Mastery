// 5. switch Statement
// Used for checking multiple values against a variable.

let day = 3;
switch (day) {
  case 1:
    console.log("sunday");
    break;

  case 2:
    console.log("Monday");
    break;

  case 3:
    console.log("Tuesday");
    break;

  case 4:
    console.log("Wedenday");
    break;
  case 5:
    console.log("Thursday");
    break;
  case 6:
    console.log("Friday");
    break;
  case 7:
    console.log("Saturday");
    break;
  default:
    console.log("Not Valid");
    break;
}
